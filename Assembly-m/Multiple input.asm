.MODEL SMALL
.STACK 100H
.CODE

MAIN PROC
    
    MOV AH,1   ;INPUT FUNCTION
    INT 21H
    
    MOV BL,AL  ;1ST INPUT
    INT 21H 
    
    MOV BH,AL  ;2ND INPUT
    INT 21H 
    
    MOV CL,AL  ;3RD INPUT
    INT 21H 
    
    MOV CH,AL  ;4TH INPUT
    
    
    MOV AH,2
    MOV DL,0AH ;NEW LINE
    INT 21H
    MOV DL,0DH
    INT 21H
    
    
    MOV DL,BL  ;1ST INPUT
    INT 21H
    
    MOV DL,BH  ;2ND INPUT
    INT 21H
    
    MOV DL,CL  ;3RD INPUT
    INT 21H
    
    MOV DL,CH  ;4TH INPUT
    INT 21H
    
  EXIT:
  MOV AH,4CH
  INT 21H
  MAIN ENDP
END MAIN
    
    